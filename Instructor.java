    class StatusCellRenderer extends DefaultTableCellRenderer {
    @Override
    public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
        Component cell = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
        
        // Change color based on the value of the status
        if (value != null) {
            String status = value.toString();
            if (status.equals("Present")) {
                cell.setForeground(new Color(48, 199, 80)); // Set green for "Present"
            } else if (status.equals("Late")) {
                cell.setForeground(new Color(233, 149, 14)); // Set red for "Late" or "Absent"
            } else if(status.equals("Absent")){
                cell.setForeground(Color.RED);
            }
            else {
                cell.setForeground(Color.BLACK); // Default background
            }
        }
        
        return cell;
    }
}
